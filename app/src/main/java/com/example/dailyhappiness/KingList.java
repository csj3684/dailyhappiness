package com.example.dailyhappiness;

public class KingList {

    int rank = 0;
    //유저 프로필도 넣어야 함
    String user = "";
    int count = 0;

    public int getRank() {
        return rank;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

}
